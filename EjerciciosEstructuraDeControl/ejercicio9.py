# 8-Escribir un programa que pida al usuario una cadena de caracteres y muestre
# por pantalla si contiene la letra "a".

cadena_caracteres = input("Ingrese una cadena de caracteres: ")

if "a" in cadena_caracteres:
    print("La cadena de caracteres contine la letra 'a'")
else:
    print("La cadena de caracteres no contine la letra 'a'")
